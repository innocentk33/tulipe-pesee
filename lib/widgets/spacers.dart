import 'package:flutter/cupertino.dart';

class VSpacer {
  static SizedBox get small => SizedBox(height: 5);

  static SizedBox get normal => SizedBox(height: 10);

  static SizedBox get large => SizedBox(height: 20);
}

class HSpacer {
  static SizedBox get small => SizedBox(width: 5);

  static SizedBox get normal => SizedBox(width: 10);

  static SizedBox get large => SizedBox(width: 20);
}
